def apply_theme(fig):
    """Apply the ERA5 dark theme to any plotly Figure.

    Non-destructif : utilise update_xaxes/update_yaxes pour MERGE avec
    une config existante (tickmode, ticktext, range, etc.) plutot que
    de l'ecraser comme le ferait update_layout(xaxis=dict(...)).
    """
    layout = {k: v for k, v in PLOTLY_LAYOUT.items()
              if k not in ("xaxis", "yaxis")}
    fig.update_layout(**layout)
    fig.update_xaxes(**PLOTLY_LAYOUT["xaxis"])
    fig.update_yaxes(**PLOTLY_LAYOUT["yaxis"])
    return fig


