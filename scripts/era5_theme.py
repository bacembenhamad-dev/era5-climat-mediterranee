def glass_card(children, extra_class=""):
    """Wrap content in a glassmorphism card div."""
    from dash import html
    return html.Div(
        children,
        className=f"glass-card {extra_class}".strip(),
    )


def kpi_card(label, value, subtext, icon_emoji, color_class, badge=None):
    """Build a KPI card component.

    color_class: one of 'kpi-indigo', 'kpi-cyan', 'kpi-green', 'kpi-orange'
    """
    from dash import html

    icon_map = {"kpi-indigo": "ind", "kpi-cyan": "cyn",
                "kpi-green": "grn", "kpi-orange": "org"}
    val_class = "kpi-value"
    if color_class in ("kpi-green",):
        val_class += " positive"
    if color_class == "kpi-orange":
        val_class += " warning"

    children = [
        html.Div([
            html.Div(label, className="kpi-label-text"),
            html.Div(icon_emoji, className=f"kpi-icon-box {icon_map.get(color_class,'ind')}"),
        ], className="kpi-head"),
        html.Div(value,   className=val_class),
        html.Div(subtext, className="kpi-subtext"),
    ]
    if badge:
        children.append(html.Div(f"✓ {badge}", className="kpi-sig-badge"))

    return html.Div(children, className=f"glass-card kpi-card {color_class}")


def section_label(text):
    from dash import html
    return html.Div(text, className="section-label")


def card_header(title, subtitle=None):
    from dash import html
    items = [html.Div(title, className="card-title")]
    if subtitle:
        items.append(html.Div(subtitle, className="card-subtitle"))
    return html.Div(items)


